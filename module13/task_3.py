# Пользователь вводит два числа — N и K.
# Напишите программу,
# которая заменяет каждое число на число,
# которое получается из исходного записью его цифр в обратном порядке,
# затем складывает их,
# снова переворачивает и выводит ответ на экран.

# Пример:

# Введите первое число: 102
# Введите второе число: 123


# Первое число наоборот: 201
# Второе число наоборот: 321

# Сумма: 522
# Сумма наоборот: 225


# Функция для переворачивания числа
def reverse_number(num):
    # Переворачиваем число, преобразовав его в строку
    return int(str(num)[::-1])

# Ввод данных пользователя
try:
    n = int(input("Введите первое число: "))
    k = int(input("Введите второе число: "))

    # Переворот чисел
    reversed_n = reverse_number(n)
    reversed_k = reverse_number(k)
    print(f"Первое число наоборот: {reversed_n}")
    print(f"Второе число наоборот: {reversed_k}")

    # Сумма чисел
    sum_reversed = reversed_n + reversed_k
    print(f"Сумма: {sum_reversed}")

    # Переворот суммы
    reversed_sum = reverse_number(sum_reversed)
    print(f"Сумма наоборот: {reversed_sum}")
except ValueError:
    print("Введены некорректные данные. Пожалуйста, введите целые числа.")
