# Функция для подсчета количества цифр в числе
def count_numbers(num):
    num_count = 0
    while num > 0:
        num_count += 1
        num = num // 10
    return num_count

# Функция для изменения числа путем перестановки первой и последней цифры
def change_number(num):
    num_count = count_numbers(num)
    if num_count < 2:
        return num  # если в числе одна цифра, возвращаем его же
    last_digit = num % 10
    first_digit = num // 10 ** (num_count - 1)
    between_digits = num % 10 ** (num_count - 1) // 10
    changed_num = last_digit * 10 ** (num_count - 1) + between_digits * 10 + first_digit
    return changed_num

# Функция main для обработки всей логики программы
def main():
    try:
        first_n = int(input("Введите первое число: "))
        if count_numbers(first_n) < 3:
            print("В первом числе меньше трёх цифр.")
            return
        second_n = int(input("Введите второе число: "))
        if count_numbers(second_n) < 4:
            print("Во втором числе меньше четырёх цифр.")
            return
    except ValueError:
        print("Пожалуйста, введите целые числа.")
        return

    first_changed = change_number(first_n)
    print("Изменённое первое число:", first_changed)
    second_changed = change_number(second_n)
    print("Изменённое второе число:", second_changed)

    sum_of_changed = first_changed + second_changed
    print("\nСумма чисел:", sum_of_changed)

# Вызов функции main
