print('Задача 4. Число наоборот')

# Вводится последовательность чисел,
# которая оканчивается нулём.
#
# Реализуйте функцию,
# которая принимает в качестве аргумента каждое число,
# переворачивает его и выводит на экран.

# Пример:
# Введите число: 1234
# Число наоборот: 4321
#
# Введите число: 1000
# Число наоборот: 0001
#
# Введите число: 0
# Программа завершена!
#
# Дополнительно: добейтесь такого вывода чисел, если в его начале идут нули.
#
# Введите число: 1230
# Число наоборот: 321
#
# Кстати, нули, которые мы убрали, называются ведущими.


# Это можно сделать, преобразовав число в строку, затем инвертировав строку и убрав ведущие нули с помощью lstrip.
# В конце преобразуем строку обратно в число для корректного отображения.

def reverse_number(number):
    # преобразуем число в строку, переворачиваем её и удаляем ведущие нули
    reversed_str = str(number)[::-1].lstrip('0')
    # если строка пуста после удаления нулей, значит число было 0 или состояло только из нулей
    reversed_number = int(reversed_str) if reversed_str else 0
    print(f"Число наоборот: {reversed_number}")


print("Введите последовательность чисел, оканчивающуюся нулём.")

while True:
    number = int(input("Введите число: "))
    if number == 0:
        print("Программа завершена!")
        break
    reverse_number(number)
