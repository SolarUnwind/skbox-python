print('Задача 3. Апгрейд калькулятора')

# Степан использует калькулятор для расчёта суммы и разности чисел, но на работе ему требуются не только обычные арифметические действия.
# Он ничего не хочет делать вручную, поэтому решил немного расширить функционал калькулятора.

# Напишите программу, запрашивающую у пользователя число и действие, которое нужно сделать с числом: вывести сумму его цифр, максимальную или минимальную цифру.

#Каждое действие оформите в виде отдельной функции, а основную программу зациклите.

# Запрошенные числа должны передаваться в функции суммы, максимума и минимума при помощи аргументов.


def sum_digits(number):
    return sum(int(digit) for digit in str(number))

def max_digit(number):
    return max(int(digit) for digit in str(number))

def min_digit(number):
    return min(int(digit) for digit in str(number))

def main():
    while True:
        user_input = input("Введите целое число (или 'выход' для завершения): ")
        if user_input.lower() == 'выход':
            break
        try:
            number = int(user_input)
        except ValueError:
            print("Это не число. Попробуйте еще раз.")
            continue

        # Запрашиваем у пользователя действие
        action = input("Введите действие: сумма, макс, мин: ").lower()

        # Выполняем действие в соответствии с введенной командой
        if action == "сумма":
            print(f"Сумма цифр числа {number}: {sum_digits(number)}")
        elif action == "макс":
            print(f"Максимальная цифра числа {number}: {max_digit(number)}")
        elif action == "мин":
            print(f"Минимальная цифра числа {number}: {min_digit(number)}")
        else:
            print("Неизвестное действие. Попробуйте снова.")

# Запуск основной программы
main()
